// ---------------------------------------------------------------------------
// Page bridge — reads window.ytInitialData from the MAIN world
// ---------------------------------------------------------------------------

let _bridgeInjected = false;

function ensureBridgeInjected() {
  if (_bridgeInjected) return Promise.resolve();
  return new Promise((resolve) => {
    const script = document.createElement("script");
    script.src = chrome.runtime.getURL("page-bridge.js");
    script.onload  = () => { script.remove(); resolve(); };
    script.onerror = () => { script.remove(); resolve(); };
    document.documentElement.appendChild(script);
    _bridgeInjected = true;
  });
}

function requestYtInitialData() {
  return new Promise((resolve) => {
    const handler = (event) => {
      if (event.source !== window) return;
      if (event.data?.type !== "AICD_YT_DATA_RESPONSE") return;
      window.removeEventListener("message", handler);
      resolve(event.data.payload ?? null);
    };
    window.addEventListener("message", handler);
    window.postMessage({ type: "AICD_REQUEST_YT_DATA" }, "*");
    setTimeout(() => { window.removeEventListener("message", handler); resolve(null); }, 3000);
  });
}

// ---------------------------------------------------------------------------
// Meta cache
// ---------------------------------------------------------------------------

let _metaCache = null;
let _pageChannelId = null; // channel page fallback — all cards share one channel

function invalidateMetaCache() {
  _metaCache = null;
  _pageChannelId = null;
  _bridgeInjected = false;
}

async function initLayer1() {
  _metaCache = new Map();
  await ensureBridgeInjected();

  // Index initial page data
  const data = await requestYtInitialData();
  if (data) {
    walkForVideos(data, _metaCache);
    // Channel pages: extract page-level channelId from the header so cards
    // that omit per-card channel info can still get a channelId.
    if (!_pageChannelId) _pageChannelId = extractPageChannelId(data);
  }

  // Index continuation data as the user scrolls or navigates
  window.addEventListener("message", (event) => {
    if (event.source !== window) return;
    if (event.data?.type !== "AICD_YT_CONTINUATION") return;
    if (!_metaCache) return;
    walkForVideos(event.data.payload, _metaCache);
  });

}

// ---------------------------------------------------------------------------
// Walker — handles both lockupViewModel (2024+) and legacy videoRenderer
// ---------------------------------------------------------------------------

function walkForVideos(root, map) {
  const stack = [root];
  while (stack.length) {
    const node = stack.pop();
    if (!node || typeof node !== "object") continue;

    if (Array.isArray(node)) {
      for (const item of node) stack.push(item);
      continue;
    }

    // ── New format: lockupViewModel ──────────────────────────────────────
    if (node.contentType === "LOCKUP_CONTENT_TYPE_VIDEO" && node.contentId) {
      const meta = buildLockupMeta(node);
      if (meta.title) map.set(node.contentId, meta);
    }

    // ── Legacy format: videoRenderer ────────────────────────────────────
    if (typeof node.videoId === "string" && node.title) {
      map.set(node.videoId, buildMeta(node));
    }

    for (const val of Object.values(node)) {
      if (val && typeof val === "object") stack.push(val);
    }
  }
}

