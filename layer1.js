/** lockupViewModel (YouTube 2024+) */
function buildLockupMeta(renderer) {
  const lmvm  = renderer.metadata?.lockupMetadataViewModel;
  const title = lmvm?.title?.content ?? "";

  // Channel name + channelId live in the first non-empty metadataRow part.
  const rows = lmvm?.metadata?.contentMetadataViewModel?.metadataRows ?? [];
  let channel = "";
  let channelId = "";
  outer: for (const row of rows) {
    for (const part of (row.metadataParts ?? [])) {
      const text = part?.text?.content ?? part?.text?.simpleText ?? "";
      if (text) {
        channel = text;
        // Try to find the browseId in commandRuns
        const runs = part?.text?.commandRuns ?? [];
        for (const run of runs) {
          const id = run?.onTap?.innertubeCommand?.browseEndpoint?.browseId;
          if (id) { channelId = id; break; }
        }
        break outer;
      }
    }
  }

  return { title, description: "", channel, channelId, badges: [], tags: [] };
}

/** Legacy videoRenderer and gridVideoRenderer */
function buildMeta(renderer) {
  // gridVideoRenderer (channel pages) uses shortBylineText instead of ownerText
  const channelId =
    renderer.ownerText?.runs?.[0]?.navigationEndpoint?.browseEndpoint?.browseId ??
    renderer.longBylineText?.runs?.[0]?.navigationEndpoint?.browseEndpoint?.browseId ??
    renderer.shortBylineText?.runs?.[0]?.navigationEndpoint?.browseEndpoint?.browseId ??
    "";
  const channel =
    extractText(renderer.ownerText ?? renderer.longBylineText ?? renderer.shortBylineText) ?? "";
  return {
    title:       extractText(renderer.title)           ?? "",
    description: extractText(renderer.descriptionSnippet) ?? "",
    channel,
    channelId,
    badges:      extractBadgeLabels(renderer),
    tags:        Array.isArray(renderer.tags) ? renderer.tags : [],
  };
}

/**
 * Extract the page-level channelId from ytInitialData.
 * Only populated on channel pages (/@handle/videos, /channel/UCxxx/...).
 */
function extractPageChannelId(data) {
  // Older YouTube: header.c4TabbedHeaderRenderer.channelId
  const fromHeader = data?.header?.c4TabbedHeaderRenderer?.channelId;
  if (fromHeader) return fromHeader;
  // Most YouTube versions: metadata.channelMetadataRenderer.externalId (reliable)
  const fromMeta = data?.metadata?.channelMetadataRenderer?.externalId;
  if (fromMeta) return fromMeta;
  // URL fallback for /channel/UCxxx paths
  const m = window.location.pathname.match(/^\/channel\/(UC[\w-]+)/);
  return m?.[1] ?? null;
}

function extractText(obj) {
  if (!obj) return "";
  if (typeof obj === "string")    return obj;
  if (obj.content)                return obj.content;   // lockupViewModel style
  if (obj.simpleText)             return obj.simpleText;
  if (Array.isArray(obj.runs))    return obj.runs.map((r) => r.text ?? "").join("");
  return "";
}

function extractBadgeLabels(renderer) {
  return [renderer.badges, renderer.ownerBadges]
    .filter(Boolean).flat()
    .map((b) => b?.metadataBadgeRenderer?.label ?? b?.metadataBadgeRenderer?.tooltip ?? "")
    .filter(Boolean)
    .map((s) => s.toLowerCase());
}

// ---------------------------------------------------------------------------
// Shadow DOM helpers
// ---------------------------------------------------------------------------

function shadowQuery(root, selector) {
  if (!root) return null;
  const hit = root.querySelector?.(selector);
  if (hit) return hit;
  for (const el of root.querySelectorAll?.("*") ?? []) {
    if (el.shadowRoot) {
      const result = shadowQuery(el.shadowRoot, selector);
      if (result) return result;
    }
  }
  return null;
}

function getVideoId(cardEl) {
  const link =
    shadowQuery(cardEl, "a#thumbnail") ??
    shadowQuery(cardEl, "a[href*='/watch']") ??
    shadowQuery(cardEl, "a[href*='/shorts/']");
  if (!link?.href) return null;
  try {
    const url = new URL(link.href);
    const v = url.searchParams.get("v");
    if (v) return v;
    // Shorts: /shorts/VIDEO_ID
    const m = url.pathname.match(/^\/shorts\/([\w-]+)/);
    return m?.[1] ?? null;
  } catch { return null; }
}

function getCardTitle(cardEl) {
  // #video-title works for legacy videoRenderer cards
  const legacyTitle =
    shadowQuery(cardEl, "#video-title")?.textContent?.trim() ??
    shadowQuery(cardEl, "yt-formatted-string#video-title")?.textContent?.trim();
  if (legacyTitle) return legacyTitle;

  // For lockupViewModel cards, the thumbnail <a> has aria-label = video title
  const thumbLabel = shadowQuery(cardEl, "a#thumbnail")?.getAttribute("aria-label");
  if (thumbLabel) return thumbLabel;

  return "";
}

function getChannelId(cardEl) {
  const videoId = getVideoId(cardEl);
  if (videoId) {
    const cached = _metaCache?.get(videoId);
    if (cached?.channelId) return cached.channelId;
  }
  // DOM fallback: find a /channel/UC... link inside the card.
  const link =
    shadowQuery(cardEl, "a.yt-simple-endpoint[href*='/channel/']") ??
    shadowQuery(cardEl, "#channel-name a") ??
    shadowQuery(cardEl, "yt-formatted-string.ytd-channel-name a");
  if (link?.href) {
    const m = link.href.match(/\/channel\/(UC[\w-]+)/);
    if (m) return m[1];
  }
  // Page-level fallback: on a channel's /videos page all cards share one channel.
  return _pageChannelId ?? "";
}

function getChannelName(cardEl) {
  const videoId = getVideoId(cardEl);
  if (videoId) {
    const cached = _metaCache?.get(videoId);
    if (cached?.channel) return cached.channel;
  }
  return shadowQuery(cardEl, "#channel-name")?.textContent?.trim() ?? "";
}

// ---------------------------------------------------------------------------
// Signal checks
// ---------------------------------------------------------------------------

function hasYoutubeDisclosure(meta) {
  return meta.badges.some(
    (b) => b.includes("ai") || b.includes("altered") || b.includes("synthetic")
  );
}

function hasAiHashtag(text) {
  return [...text.matchAll(/#\w+/g)]
    .map((m) => m[0].toLowerCase())
    .some((tag) => AI_HASHTAGS.has(tag));
}

function hasAiTool(text) {
  return AI_TOOLS.some((tool) => text.includes(tool));
}

function hasAiPhrase(text) {
  return AI_PHRASES.some((phrase) => text.includes(phrase));
}

function hasAiSubjectVerb(title) {
  return AI_SUBJECT_VERBS.test(title);
}

function hasAiChannelName(name) {
  return /\bai\b|a\.i\.|artificial intelligence|generated|syntheti/.test(name);
}

